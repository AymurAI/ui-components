// ── Public API ────────────────────────────────────────────────────────────────

export type LogoVariant = "logo" | "logo-feature" | "iso";

export interface LogoProps extends HTMLAttributes<HTMLDivElement> {
  /** Which logo version to render */
  variant?: LogoVariant;
  /**
   * Feature/app name shown in the "Logo+Feature" variant (e.g. "Anonimizador").
   * Falls back to "Aymurai" when omitted.
   */
  featureName?: string;
}

export function Logo({
  variant = "logo",
  featureName = "Anonimizador",
  className,
  ...props
}: LogoProps) {
  if (variant === "iso") {
    return (
      <div
        {...props}
        className={cx(
          css({
            display: "inline-flex",
            alignItems: "center",
            overflow: "hidden",
          }),
          className,
        )}
        role="img"
        aria-label="AymurAI"
      >
        <IsoMark />
      </div>
    );
  }

  if (variant === "logo-feature") {
    return (
      <div
        {...props}
        className={cx(
          css({
            display: "inline-flex",
            alignItems: "center",
            gap: "4", // 16px
            h: "10",
            overflow: "hidden",
          }),
          className,
        )}
        role="img"
        aria-label={`AymurAI ${featureName}`}
      >
        <IsoMark />
        {/* Vertical divider */}
        <div
          className={css({
            w: "[2px]",
            h: "[16px]",
            bg: "text.default",
            flexShrink: "0",
          })}
          aria-hidden
        />
        <span
          className={css({
            textStyle: "subtitle.md.strong",
            color: "text.default",
            whiteSpace: "nowrap",
          })}
        >
          {featureName}
        </span>
      </div>
    );
  }

  // variant === "logo"
  return (
    <div
      {...props}
      className={cx(
        css({
          display: "inline-flex",
          alignItems: "center",
          gap: "2", // 8px
          h: "10", // 40px — matches Figma Version=Logo height
          overflow: "hidden",
        }),
        className,
      )}
      role="img"
      aria-label="AymurAI"
    >
      <IsoMark />
      <Wordmark />
    </div>
  );
}

export default Logo;
